public class ArithOp {

	public static void main(String[] args) {
		int a = 20, b = 30, c;
		
		c = a + b;
		System.out.println("Addition is "+ c);
		
		c = a - b;
		System.out.println("Substraction is " + c);
		
		c = a * b;
		System.out.println("Multiplication is " + c);
		
		c = a / b;
		System.out.println("Division is " + c);

	}

}
